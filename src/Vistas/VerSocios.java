    public void cargarSocios(){
        DefaultTableModel mdl = (DefaultTableModel) tSocios.getModel();
        Iterator<Socio> it = main.socios.iterator();
        mdl.setRowCount(0);
        while (it.hasNext()) {
            Socio s = it.next();
            if (s.isVigente()) {  
                Object[] fila = new Object[5];
                fila[0] = s.getCi();
                fila[1] = s;
                fila[2] = s.getApellido();
                fila[3] = s.getTelefono();
                fila[4] = s.getDireccion();
                mdl.addRow(fila); 
            }
        }
    }
